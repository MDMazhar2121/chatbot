# SMART MATCHING FUNCTION
# ================================
def similarity(a, b):
    return SequenceMatcher(None, a.lower(), b.lower()).ratio()

def chatbot(user_input):
    user_input = user_input.strip()
    if not user_input:
        return "कृपया कोई प्रश्न पूछें।"

    # Exact match
    for item in data:
        if user_input == item["question"]:
            return item["answer"]

    # Fuzzy match
    best_match = None
    best_score = 0
    for item in data:
        score = similarity(user_input, item["question"])
        if score > best_score:
            best_score = score
            best_match = item

    if best_score > 0.6:
        return best_match["answer"]

    return "मुझे इस प्रश्न का उत्तर नहीं पता। कृपया रामायण, महाभारत, या हिंदू देवताओं के बारे में पूछें।"

